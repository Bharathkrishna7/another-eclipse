package com.Opps.MultipleInheritance;

public class GrandParentEncaptulationDetails {

	private String name;
	private String surName;
	private double bankbal;
	private double lands;
	private int cars;
	private int houses;

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSurName() {
		return surName;
	}

	public void setSurName(String surName) {
		this.surName = surName;
	}

	public double getBankbal() {
		return bankbal;
	}

	public void setBankbal(double bankbal) {
		this.bankbal = bankbal;
	}

	public double getLands() {
		return lands;
	}

	public void setLands(double lands) {
		this.lands = lands;
	}

	public int getCars() {
		return cars;
	}

	public void setCars(int cars) {
		this.cars = cars;
	}

	public int getHouses() {
		return houses;
	}

	public void setHouses(int houses) {
		this.houses = houses;
	}

}
